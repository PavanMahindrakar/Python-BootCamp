name = "Pavan0123456789"

print(name[0:2]) # Accessing characters from index 0 to 1 (exclusive of 2)

print(name[2:-1]) # Accessing characters from index 2 to -2 (exclusive of -1) #this same as name[2:4]

print(name[0:10:1] ) # Accessing characters from index 0 to 9 with a step of 1

print(name[0:10:2]) # Accessing characters from index 0 to 9 with a step of 2

print(name[0:10:3]) # Accessing characters from index 0 to 9 with a step of 3

print(name[:4]) # Accessing characters from the start to index 3 (exclusive of 4)
print(name[1:]) # Accessing characters from index 4 to the end of the string