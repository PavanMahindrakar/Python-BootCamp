#dict methods 
marks = {"Math": 95, "English": 88, "Science": 92}
print(marks)

print(marks.keys())  # Get all keys
print(marks.values())  # Get all values
print(marks.items())  # Get all key-value pairs
print("Math" in marks)  # Check if a key exists
print("History" in marks)  # Check if a key does not exist  